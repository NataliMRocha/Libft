#include "libft.h"

void	bzero(void *s, size_t n)
{
	unsigned int	i;
	uint8_t			*b;

	i = 0;
	b = (char *) s;
	while (i < n)
	{
		b[i] = '\0';
		i++;
	}
	return ;
}
